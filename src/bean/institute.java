package bean;

public class institute {

    private int instituteNo;
    private String instituteName;
    private String coach;
    private String coachTel;

    public int getInstituteNo() {
        return instituteNo;
    }

    public String getInstituteName() {
        return instituteName;
    }

    public String getCoach() {
        return coach;
    }

    public String getCoachTel() {
        return coachTel;
    }

    public void setInstituteNo(int instituteNo) {
        this.instituteNo = instituteNo;
    }

    public void setInstituteName(String instituteName) {
        this.instituteName = instituteName;
    }

    public void setCoach(String coach) {
        this.coach = coach;
    }

    public void setCoachTel(String coachTel) {
        this.coachTel = coachTel;
    }
}
